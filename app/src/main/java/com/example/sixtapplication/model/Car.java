package com.example.sixtapplication.model;

public class Car {

    // Class for Car Objects

    private  String id;
    private  String modelIdentifier;
    private  String modelName;
    private  String name;
    private  String make;
    private  String group;
    private  String color;
    private  String series;
    private  String fuelType;
    private  String fuelLevel;
    private  String transmission;
    private  String licensePlate;
    private  String latitude;
    private  String longitude;
    private  String innerCleanliness;
    private  String carImageUrl;

    public Car(String id, String modelIdentifier, String modelName, String name, String make, String group, String color, String series, String fuelType, String fuelLevel, String transmission, String licensePlate, String latitude, String longitude, String innerCleanliness, String carImageUrl) {
        this.id = id;
        this.modelIdentifier = modelIdentifier;
        this.modelName = modelName;
        this.name = name;
        this.make = make;
        this.group = group;
        this.color = color;
        this.series = series;
        this.fuelType = fuelType;
        this.fuelLevel = fuelLevel;
        this.transmission = transmission;
        this.licensePlate = licensePlate;
        this.latitude = latitude;
        this.longitude = longitude;
        this.innerCleanliness = innerCleanliness;
        this.carImageUrl = carImageUrl;
    }


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getModelIdentifier() {
        return modelIdentifier;
    }

    public void setModelIdentifier(String modelIdentifier) {
        this.modelIdentifier = modelIdentifier;
    }

    public String getModel_name() {
        return modelName;
    }

    public void setModel_name(String model_name) {
        this.modelName = model_name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMake() {
        return make;
    }

    public void setMake(String make) {
        this.make = make;
    }

    public String getGroup() {
        return group;
    }

    public void setGroup(String group) {
        this.group = group;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getSeries() {
        return series;
    }

    public void setSeries(String series) {
        this.series = series;
    }

    public String getFuelType() {
        return fuelType;
    }

    public void setFuelType(String fuelType) {
        this.fuelType = fuelType;
    }

    public String getFuelLevel() {
        return fuelLevel;
    }

    public void setFuelLevel(String fuelLevel) {
        this.fuelLevel = fuelLevel;
    }

    public String getTransmission() {
        return transmission;
    }

    public void setTransmission(String transmission) {
        this.transmission = transmission;
    }

    public String getLicensePlate() {
        return licensePlate;
    }

    public void setLicensePlate(String licensePlate) {
        this.licensePlate = licensePlate;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getInnerCleanliness() {
        return innerCleanliness;
    }

    public void setInnerCleanliness(String innerCleanliness) {
        this.innerCleanliness = innerCleanliness;
    }

    public String getCarImageUrl() {
        return carImageUrl;
    }

    public void setCarImageUrl(String carImageUrl) {
        this.carImageUrl = carImageUrl;
    }




}
    
    
    
    




